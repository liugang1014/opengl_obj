uniform mat4 uMVPMatrix; //??任????
uniform mat4 uMMatrix; //?任????
uniform vec3 uLightLocation;	//???λ??
uniform vec3 uCamera;	//?????λ??
attribute vec3 aPosition;  //????λ??
attribute vec3 aNormal;    //????????
attribute vec2 aTexCoor;    //????????????
//????????????????????
varying vec4 ambient;
varying vec4 diffuse;
varying vec4 specular;
varying vec2 vTextureCoord;  
//??λ????????????
void pointLight(					//??λ????????????
  in vec3 normal,				//??????
  inout vec4 ambient,			//?????????????
  inout vec4 diffuse,				//???????????
  inout vec4 specular,			//????????????
  in vec3 lightLocation,			//???λ??
  in vec4 lightAmbient,			//?????????
  in vec4 lightDiffuse,			//???????
  in vec4 lightSpecular			//????????
){
  ambient=lightAmbient;			//???ó???????????????  
  vec3 normalTarget=aPosition+normal;	//????任????????
  vec3 newNormal=(uMMatrix*vec4(normalTarget,1)).xyz-(uMMatrix*vec4(aPosition,1)).xyz;
  newNormal=normalize(newNormal); 	//??????????
  //????????????????????
  vec3 eye= normalize(uCamera-(uMMatrix*vec4(aPosition,1)).xyz);  
  //????????????λ???????vp
  vec3 vp= normalize(lightLocation-(uMMatrix*vec4(aPosition,1)).xyz);  
  vp=normalize(vp);//?????vp
  vec3 halfVector=normalize(vp+eye);	//?????????????????    
  float shininess=50.0;				//??????С???
  float nDotViewPosition=max(0.0,dot(newNormal,vp)); 	//????????vp??????0??????
  diffuse=lightDiffuse*nDotViewPosition;				//????????????????
  float nDotViewHalfVector=dot(newNormal,halfVector);	//??????????????? 
  float powerFactor=max(0.0,pow(nDotViewHalfVector,shininess)); 	//??????????????
  specular=lightSpecular*powerFactor;    			//???????????????
}


void main()     
{ 
   gl_Position = uMVPMatrix * vec4(aPosition,1); //??????任????????λ???????λ??  
   
   vec4 ambientTemp, diffuseTemp, specularTemp;   //???????????????????????????      
   pointLight(normalize(aNormal),ambientTemp,diffuseTemp,specularTemp,uLightLocation,vec4(0.15,0.15,0.15,1.0),vec4(0.9,0.9,0.9,1.0),vec4(0.4,0.4,0.4,1.0));
   
   ambient=ambientTemp;
   diffuse=diffuseTemp;
   specular=specularTemp;
   vTextureCoord = aTexCoor;//?????????????????????????
}                      